export type DictKey = keyof typeof dict["en"];

const Ctx = createContext<{ locale: Locale; t: (k: DictKey) => string; toggle: () => void }>({
  locale: "en",
  t: (k) => dict.en[k],
  toggle: () => {},
});

export const useLocale = () => useContext(Ctx);

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Locale>("en");

  useEffect(() => {
    const stored = localStorage.getItem("pc_locale") as Locale | null;
    if (stored) setLocale(stored);
  }, []);

  const toggle = useCallback(() => {
    setLocale((prev) => {
      const next = prev === "en" ? "ja" : "en";
      localStorage.setItem("pc_locale", next);
      return next;
    });
  }, []);

  const t = useCallback((k: DictKey) => dict[locale][k], [locale]);

  return <Ctx.Provider value={{ locale, t, toggle }}>{children}</Ctx.Provider>;
}
